    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(setNumber == 1) {
            romOptions.s1name = jTextField1.getText();
            romOptions.s1file1 = jTextField2.getText();
            romOptions.s1file2 = jTextField3.getText();
            romOptions.s1file3 = jTextField4.getText();
        }
        else if(setNumber == 2) {
            romOptions.s2name = jTextField1.getText();
            romOptions.s2file1 = jTextField2.getText();
            romOptions.s2file2 = jTextField3.getText();
            romOptions.s2file3 = jTextField4.getText();
        }
        else if(setNumber == 3) {
            romOptions.s3name = jTextField1.getText();
            romOptions.s3file1 = jTextField2.getText();
            romOptions.s3file2 = jTextField3.getText();
            romOptions.s3file3 = jTextField4.getText();
        }
        romOptions.update();
        this.setVisible(false);
    }//GEN-LAST:event_jButton4ActionPerformed

