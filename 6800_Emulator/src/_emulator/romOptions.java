    public void setFiles(Options opt, int set) {
        romOptions = opt;
        setNumber = set;
        if(set == 1) {
            jTextField1.setText(opt.s1name);
            jTextField2.setText(opt.s1file1);
            jTextField3.setText(opt.s1file2);
            jTextField4.setText(opt.s1file3);
        }
        else if(set == 2) {
            jTextField1.setText(opt.s2name);
            jTextField2.setText(opt.s2file1);
            jTextField3.setText(opt.s2file2);
            jTextField4.setText(opt.s2file3);
        }
        else if(set == 3) {
            jTextField1.setText(opt.s3name);
            jTextField2.setText(opt.s3file1);
            jTextField3.setText(opt.s3file2);
            jTextField4.setText(opt.s3file3);
        }
    }

