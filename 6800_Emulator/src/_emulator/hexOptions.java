    public void setCaller(button b) {
        caller = b;
        if(caller.mem != 0)
            jTextField1.setText("0x" +Integer.toHexString(caller.mem));

        switch(caller.irq) {
            case 0:
                jRadioButton1.setSelected(true);
                break;
            case 1:
                jRadioButton2.setSelected(true);
                break;
            case 2:
                jRadioButton3.setSelected(true);
        }//IRQ switch
    }//setCaller

