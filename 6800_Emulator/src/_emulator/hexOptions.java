    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        testHarness.fileParser f = new testHarness.fileParser();
        caller.mem = f.parseHex(jTextField1.getText(),jTextField1.getText().length());

        if(jRadioButton1.isSelected())
            caller.irq = 0;
        else if(jRadioButton2.isSelected())
            caller.irq = 1;
        else if(jRadioButton3.isSelected())
            caller.irq = 2;

        button b = l.get(caller.listLoc);
        b.mem = caller.mem;
        b.irq = caller.irq;
        b.ID = caller.ID;
        l.set(caller.listLoc, b);

        for(int i = 0; i < l.size(); i ++) {
            b = l.get(i);
            if(b.type.compareTo("key") == 0) {
                b.mem = caller.mem;

                //set encoding
                if(jRadioButton4.isSelected())
                    b.encoding = true;
                else if(jRadioButton5.isSelected())
                    b.encoding = false;
                
                l.set(b.listLoc,b);
            }
        }//loop and set all hexpad locs


        this.setVisible(false);
}//GEN-LAST:event_jButton1ActionPerformed

