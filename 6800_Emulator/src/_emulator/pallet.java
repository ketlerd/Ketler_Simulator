    private void formKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyTyped
        if(key) {
            System.out.println(evt.getKeyChar() + "  " +(int)evt.getKeyChar());
            proc.mem[keyAddress] = (byte)evt.getKeyChar();
            System.out.println(Integer.toHexString(keyAddress));
            proc.updated = true;

            ArrayList<button> l = palletP1.list;
            for(int i = 0; i < l.size(); i++) {
                if(l.get(i).type.compareTo("kb") == 0) {
                    if(l.get(i).irq == 1)
                        proc.MI = true;
                    else if(l.get(i).irq == 2)
                        proc.NMI = true;

                    proc.updated = true;
                }//irq if
            }//loop for keyboard
        }
    }//GEN-LAST:event_formKeyTyped

