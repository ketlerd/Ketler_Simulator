    /**
     * getObjs
     * returns an int array representing the objects in the pallet
     * in the following form:
     * memory address
     * IRQ
     * Kind of pallet object: 0 Display, 1 Switch
     * @return int[]
     */
    public int[] getObjs() {
       ArrayList<button> l = palletP1.list;
       int[] objs = new int[l.size()*3];

       for(int i = 0; i < l.size(); i++) {  
           objs[i*3] = l.get(i).mem;
           objs[i*3 + 1] = l.get(i).irq;
           objs[i*3 + 2] = l.get(i).kind;
       }

       return objs;
    }

