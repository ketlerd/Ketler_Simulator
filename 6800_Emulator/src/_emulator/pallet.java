    private void startUpdater() {
        if(updater)
            return;
        else {
            try {
                Thread updateInterface = new Thread(updateP);
                updateP.p = palletP1;
                updateP.processor = proc;
                updateInterface.start();
            } catch (Exception e) {
                System.out.println(e.toString());
            }
            updater = true;
        }
    }//startUpdater

